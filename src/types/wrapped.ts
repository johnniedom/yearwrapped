export type GradientType = 'magenta' | 'sunset' | 'aurora' | 'cosmic' | 'golden' | 'ocean' | 'forest' | 'berry' | 'fire' | 'midnight';

export interface CategoryField {
  id: string;
  label: string;
  type: 'text' | 'textarea' | 'image' | 'number';
  placeholder?: string;
  required?: boolean;
}

export interface Subcategory {
  id: string;
  title: string;
  description: string;
  icon: string;
  gradient: GradientType;
  fields: CategoryField[];
}

export interface MainCategory {
  id: string;
  title: string;
  description: string;
  icon: string;
  gradient: GradientType;
  subcategories: Subcategory[];
}

// Legacy support - alias Subcategory as Category
export type Category = Subcategory;

export interface WrappedCard {
  id: string;
  categoryId: string;
  subcategoryId?: string;
  data: Record<string, string | number>;
  imageUrl?: string;
  createdAt: Date;
}

export interface CustomCategory {
  id: string;
  title: string;
  icon: string;
  gradient: GradientType;
  questions: CustomQuestion[];
  createdAt: Date;
}

export interface CustomQuestion {
  id: string;
  question: string;
  type: 'text' | 'textarea' | 'image' | 'number' | 'list';
  placeholder?: string;
}

